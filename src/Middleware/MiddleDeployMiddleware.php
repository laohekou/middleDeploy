<?php

namespace Yan9\MiddleDeploy\Middleware;

use Auth;
use Closure;

class MiddleDeployMiddleware
{
    /**
     * Run the request filter.
     *
     * @param \Illuminate\Http\Request $request
     * @param \Closure                 $next
     *
     * @return mixed
     */
    public function handle($request, Closure $next)
    {
        return $next($request);
    }
}
